// ─── Helper functions ───────────────────────────────────────────────────────

/**
 * Get summary counts by status
 */
export function getStatusSummary() {
  const counts: Record<PageStatus, number> = {
    validated: 0,
    partial: 0,
    placeholder: 0,
    'coming-soon': 0,
  };
  Object.values(PAGE_STATUS).forEach(({ status }) => counts[status]++);
  return counts;
}

/**
 * Get all pages by status
 */
export function getPagesByStatus(status: PageStatus): PageStatusEntry[] {
  return Object.values(PAGE_STATUS).filter(p => p.status === status);
}

/**
 * Get all pages by category
 */
export function getPagesByCategory(category: string): PageStatusEntry[] {
  return Object.values(PAGE_STATUS).filter(p => p.category === category);
}
